#include "./libft.h"

char		*ft_strncpy(char *dst, const char *src, size_t len)
{
	char	*ptr;

	ptr = dst;
	while (len--)
		*src ? (*dst++ = *src++)
			: (*dst++ = '\0');
	return (ptr);
}
